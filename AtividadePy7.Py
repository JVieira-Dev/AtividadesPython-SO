comprimento = int(input("Digite o comprimento do paralelepípedo em centímetros: "))
largura = int(input("Digite a largura do paralelepípedo em centímetros: "))
altura = int(input("Digite a altura do paralelepípedo em centímetros: "))

volume = comprimento * largura * altura

print("O volume do paralelepípedo é de", volume, "cm³.")